<h2>Who we Are</h2>
<p>Leicestershire &amp; Rutland Chess Association is the body recognised by the <a href="http://www.englishchess.org.uk/">English Chess Federation</a> as being responsible for chess in the city of Leicester and the counties of Leicestershire and Rutland.  The Association is affiliated to the <a href="http://www.mccu.org.uk/">Midland Counties Chess Union</a>.</p>

<h2>What We Do</h2>
<p>Control, organise and co-ordinate all County chess affairs and support independent events.</p>

<p>Act as the organisation recognised by the English Chess Federation, and the Midland Counties Chess Union as the governing body of chess in Leicestershire and Rutland, and to affiliate to those bodies.</p>

<p>Encourage the establishment of chess clubs in Leicestershire and Rutland and to foster chess playing generally.</p>

<p>Run county teams at all levels (including junior u/12, u/14, u/16 and u/18).</p>

